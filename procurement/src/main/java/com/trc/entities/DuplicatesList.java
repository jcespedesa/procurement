package com.trc.entities;


public class DuplicatesList 
{
	String clientId;
	String cname;
	String repeatedRecords;
	
	
	public String getClientId() 
	{
		return clientId;
	}
	
	public void setClientId(String clientId) 
	{
		this.clientId=clientId;
	}
	
	public String getRepeatedRecords() 
	{
		return repeatedRecords;
	}
	
	public void setRepeatedRecords(String repeatedRecords) 
	{
		this.repeatedRecords=repeatedRecords;
	}

	public String getCname() 
	{
		return cname;
	}

	public void setCname(String cname) 
	{
		this.cname=cname;
	}
	
	
}
